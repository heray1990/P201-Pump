stc_lcd_d61593a_lcdram_t LCD_D61593A_GenRam_Channel(uint8_t u8Val, boolean_t bDisplay)
{
    stc_lcd_d61593a_lcdram_t retStruct;

    retStruct.u8LcdRamIdx = LCDRAM_INDEX_2;
    if(TRUE == bDisplay)
    {
        retStruct.unRamData.u32_dis = ((u8Num1To11Table[u8Val] | CHAR_T1) << 8) & 0x0000ff00;
    }
    else
    {
        retStruct.unRamData.u32_dis = 0xffff00ff;    // 不显示 T1 和 1
    }

    return retStruct;
}


